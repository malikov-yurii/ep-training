public class MathUtils {

    public static Double sumDoubles(Double first, Double second){
        return first + second;
    }

    public static Float sumFloats(Float first, Float second){
        return first + second;
    }

    public static Long sumLongs(Long first, Long second){
        return first + second;
    }

    public static Integer sumInts(Integer first, Integer second){
        return first + second;
    }

    public static Double subtractDoubles(Double first, Double second){
        return first - second;
    }

    public static Float subtractFloats(Float first, Float second){
        return first - second;
    }

    public static Long subtractLongs(Long first, Long second){
        return first - second;
    }

    public static Integer subtractInts(Integer first, Integer second){
        return first - second;
    }

    public static Double multiplyDoubles(Double first, Double second){
        return first * second;
    }

    public static Float multiplyFloats(Float first, Float second){
        return first * second;
    }

    public static Long multiplyLongs(Long first, Long second){
        return first * second;
    }

    public static Integer multiplyInts(Integer first, Integer second){
        return first + second;
    }

}
